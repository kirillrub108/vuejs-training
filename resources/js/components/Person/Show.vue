<script>
import {mapGetters} from "vuex";

export default {
    name: "Show",

    mounted() {
        this.$store.dispatch('getPerson', this.$route.params.id)
    },

    computed: {
        ...mapGetters({
            person: 'person',
        })
    }
}
</script>

<template>
    <div v-if="person">
        <div>
            Name: {{ person.name }}
        </div>
        <div>
            Age: {{ person.age }}
        </div>
        <div>
            Job: {{ person.job }}
        </div>
        <div>
            <router-link :to="{ name: 'person.edit', params: { id: person.id } }" class="btn btn-outline-success">Edit</router-link>
        </div>
    </div>
</template>

